public class CountDown1
{
	public static void main(String[] args)
	{
	int i = 10;
	System.out.println("CountDown to Launch ");
	System.out.println(i);
	i--;
	System.out.println(i);
	i--;
	System.out.println(i);
	i--;
	System.out.println(i);
	i--;
	System.out.println(i);
	i--;
	System.out.println(i);
	i--;
	System.out.println(i);
	i--;
	System.out.println(i);
	i--;
	System.out.println(i);
	i--;
	System.out.println(i);
	System.out.println("Blast off!");
	}
};